public class NamesArray {
    public static void main(String[] args) {
        String[] names = {
            "Vaibhavi", "Rahul", "Priya", "Amit", "Sneha",
            "Kiran", "Rohan", "Anjali", "Neha", "Arjun"
        };

        for (String name : names) {
            System.out.println(name);
        }
    }
}
